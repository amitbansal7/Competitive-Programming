class RangeEncoding {
public:
	int minRanges(vector <int>);
};

int RangeEncoding::minRanges(vector <int> arr) 
{
	int range = 1;
	vector <int>::iterator it;
	for(it = arr.begin();it != arr.end() -1 ;it++)
		if((*(it+1) - *it) > 1)
			range++;

	return range;
}


